public class Person {
    // 类的属性（成员变量）
    private String name;
    private int age;

    // 构造函数
    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }
    


    // getter 方法
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
